//Write a Java program to find the area and perimeter of a circle.

public class Rahadul_6 {
  public static void main(String[] args) {
    double rad, area, perimeter;
    rad = 8;
    area = (rad*rad)*3.14159;
    perimeter = 2*3.14159*rad;
    System.out.println("Celcius value is: "+area);
    System.out.println("Celcius value is: "+perimeter);
  }
}
